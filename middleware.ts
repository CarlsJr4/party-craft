import { createServerClient, type CookieOptions } from '@supabase/ssr';
import { NextResponse, type NextRequest } from 'next/server';

export async function middleware(request: NextRequest) {
  let response = NextResponse.next({
    request: {
      headers: request.headers,
    },
  });

  const supabase = createServerClient<Database>(
    process.env.NEXT_PUBLIC_SUPABASE_URL!,
    process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY!,
    {
      cookies: {
        get(name: string) {
          return request.cookies.get(name)?.value;
        },
        set(name: string, value: string, options: CookieOptions) {
          request.cookies.set({
            name,
            value,
            ...options,
          });
          response = NextResponse.next({
            request: {
              headers: request.headers,
            },
          });
          response.cookies.set({
            name,
            value,
            ...options,
          });
        },
        remove(name: string, options: CookieOptions) {
          request.cookies.set({
            name,
            value: '',
            ...options,
          });
          response = NextResponse.next({
            request: {
              headers: request.headers,
            },
          });
          response.cookies.set({
            name,
            value: '',
            ...options,
          });
        },
      },
    }
  );

  let supabaseSession = await supabase.auth.getSession();
  const isAuth = supabaseSession.data.session !== null;

  // If user is authenticated and user is on login page, redirect to dashboard
  if (isAuth && request.nextUrl.pathname === '/') {
    return NextResponse.redirect(new URL('/dashboard/explore', request.url));
  }

  // Protected routes
  if (!isAuth && request.nextUrl.pathname.includes('dashboard')) {
    return NextResponse.redirect(new URL('/', request.url));
  }

  return response;
}
